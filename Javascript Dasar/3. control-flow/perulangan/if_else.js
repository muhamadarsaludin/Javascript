var jmlAngkot = 10;
var angkotBeroprasi = 7;

for (var noAngkot = 1; noAngkot <= jmlAngkot; noAngkot++) {
    if (noAngkot <= angkotBeroprasi) {
        console.log('Angkot No.' + noAngkot + ' Beroperasi dengan Baik.');
    } else {
        console.log('Angkot No.' + noAngkot + ' Tidak Beroperasi');

    }
}